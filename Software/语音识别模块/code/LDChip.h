#define CODE_GD 0x05			//关灯
#define CODE_BJ 0x16			//关灯
#define CODE_SH 0x17			//上海
#define CODE_GZ	0x2f			//广州
#define CODE_FY 0x06			//翻页
#define CODE_DKDJ 0x07			//打开电机
#define CODE_GBDJ 0x08			//关闭电机
#define CODE_AS 0x09			//加速
#define CODE_SS 0x010			//减速
#define CODE_KJ 0x011			//减速
#define CODE_GJ 0x012			//减速




#define MIC_VOL 0x30	 //咪头增益（灵敏度调节）
#endif
